package com.qkwl.common.dto.user;

public class City {

    private int code;
    private String ZN;
    private String EN;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getZN() {
        return ZN;
    }

    public void setZN(String ZN) {
        this.ZN = ZN;
    }

    public String getEN() {
        return EN;
    }

    public void setEN(String EN) {
        this.EN = EN;
    }
}
